#include "includes/libft.h"

char	*ft_strchr(const char *s, int n)
{
	int	i;

	i = 0;
	while (*(s + i))
	{
		if (*(s + i) == n)
			return ((char *)(s + i));
		i++;
	}
	if (n == '\0')
		return ((char *)(s + i));
	return (NULL);
}
